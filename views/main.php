                <p class="text"><b><strong>Tuckpointing</strong></b> is vital to the integrity and preservation of a brick structure. Tuckpointing is placing new mortar in weathered brickwork joints. Replacing worn or missing mortar helps keep water from working its way through masonry structures. When old mortar is missing, moisture can seep into spots where it was not intended. Over time–and without repairs–it can lead to structural issues.<br><br>
                    <b><strong>Masonry</strong></b> is the building of structures from individual units, which are often laid in and bound together by mortar; the term masonry can also refer to the units themselves. The common materials of masonry construction are brick, building stone such as marble, granite, and limestone, cast stone, concrete block, glass block, and adobe. Masonry is generally a highly durable form of construction. However, the materials used, the quality of the mortar and workmanship, and the pattern in which the units are assembled can substantially affect the durability of the overall masonry construction. A person who constructs masonry is called a mason or bricklayer. These are both classified as construction trades.
